public class FloatingNumbers {

	public static void main(String[] args) 
	{
		//Float Numbers
		float smallestFloatValue = Float.MIN_VALUE;
		float biggestFloatValue = Float.MAX_VALUE;
		System.out.println("Smallest Float value = "+ smallestFloatValue);
		System.out.println("Biggest Float Value = " + biggestFloatValue);

		//Double Numbers
		double smallestdoubleValue = Double.MIN_VALUE;
		double biggestdoubleValue = Double.MAX_VALUE;
		System.out.println("Smallest double value = "+ smallestdoubleValue);
		System.out.println("Biggest double Value = " + biggestdoubleValue);

		//Casting
		int intNumber = 5;
		
		float floatNumber =(float) (5.343);
		double doubleNumber = 232.323;
		
		
	}

}
